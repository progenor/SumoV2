class Display
{
public:
    Display();

    // Initialize display display
    bool setup();

    // Display text with optional numeric value
    void print(const char *text, double value = -1);

    // Clear display
    void clear();

    // Display loading screen with custom bitmap
    void drawLoadingScreen(const char *status = "Loading...");
    void drawMainScreen(void);

    // Display IR sensor bars (0-1 digital values)
    void displayIR(int *irValues, int sensorCount);

    // Throttled display update (respects DISPLAY_REFRESH_MS)
    bool shouldUpdate();

    // Menu screen methods
    void drawSpeedSelectorScreen(int currentSpeedLevel);
    void drawSettingsScreen(const char *motorStatus = "ENABLED",
                            const char *currentA = "0mA",
                            const char *currentB = "0mA");
    void drawSensorReadingsScreen(int *irValues, int irCount);
    void drawStatusScreen(RobotMode mode, bool isPaused, int currentScreen);
    void drawStrategySelectorScreen(int currentStrategy);
    void drawDirectionIndicatorScreen(int motorDirection, int leftMotorPWM, int rightMotorPWM);
    void drawBatteryTemperatureScreen(float batteryVoltage, float temperatureC);

private:
    Adafruit_SSD1306 display;
    unsigned long lastUpdateTime;

    // Helper to draw bar graph
    void drawBarGraph(uint8_t *values, uint8_t count, const char *label);
};

